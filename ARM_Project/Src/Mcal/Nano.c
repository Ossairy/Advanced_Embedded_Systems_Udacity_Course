/******************************************************************************
* \Syntax          : void SYSTICK_Init (void)
* \Description     : Initialize the SYSTICK timer .
*
* \Sync\Async      : Synchronous   
* \Reentrancy      : Non Reentrant       
* \Parameters (in) : None                     
* \Parameters (out): None                                                      
* \Return value:   : None
*******************************************************************************/

void SYSTICK_Init(void){

STRELOAD.R=15999999 ; /* TO CREATE 2 SECOND TIME PERIOD as an initial condition */
STCURRENT.R=0;
	
STCTRL.B.CLK_SRC=1;
STCTRL.B.INTEN=1;
STCTRL.B.enable=1;

}


/******************************************************************************
* \Syntax          : void SYSTICK_SetPeriod (uint32 Period)
* \Description     : Set the period of the SYSTICK timer .
*
* \Sync\Async      : Synchronous   
* \Reentrancy      : Non Reentrant       
* \Parameters (in) : uint32 Period                     
* \Parameters (out): None                                                      
* \Return value:   : None
*******************************************************************************/


void SYSTICK_SetPeriod(uint32 Period){
	
STRELOAD.R=(15999999*Period) ; ///* Period in seconds *//*
STCURRENT.R=0;

	
}




